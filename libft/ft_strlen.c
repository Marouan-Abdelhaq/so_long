#include "libft.h"

size_t	ft_strlen(const char *str)
{
	int	i;

	i = 0;
	while (str[i] == ' ' || str[i] == '\t')
		i++;
	if (str[i])
	{
		while (str[i])
			i++;
		return (i);
	}
	return (0);
}
