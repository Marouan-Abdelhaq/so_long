#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*o;

	o = 0;
	while (*s)
	{
		if (c == *s)
		{
			o = (char *)s;
		}
		s++;
	}
	if (c == '\0')
	{
		return ((char *)s);
	}
	return (o);
}
